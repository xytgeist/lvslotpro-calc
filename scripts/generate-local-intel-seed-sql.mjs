import fs from 'node:fs'
import path from 'node:path'

function parseCsv(text) {
  const rows = []
  let i = 0
  let field = ''
  let row = []
  let inQuotes = false

  const pushField = () => {
    row.push(field)
    field = ''
  }
  const pushRow = () => {
    // Skip completely empty trailing row
    if (row.length === 1 && row[0] === '') return
    rows.push(row)
    row = []
  }

  while (i < text.length) {
    const ch = text[i]

    if (inQuotes) {
      if (ch === '"') {
        const next = text[i + 1]
        if (next === '"') {
          field += '"'
          i += 2
          continue
        }
        inQuotes = false
        i += 1
        continue
      }
      field += ch
      i += 1
      continue
    }

    if (ch === '"') {
      inQuotes = true
      i += 1
      continue
    }

    if (ch === ',') {
      pushField()
      i += 1
      continue
    }

    if (ch === '\r') {
      i += 1
      continue
    }

    if (ch === '\n') {
      pushField()
      pushRow()
      i += 1
      continue
    }

    field += ch
    i += 1
  }

  // last row
  pushField()
  pushRow()

  if (rows.length === 0) return { header: [], records: [] }
  const header = rows[0].map((h) => h.trim())
  const records = rows.slice(1).map((r) => {
    const obj = {}
    header.forEach((h, idx) => {
      obj[h] = (r[idx] ?? '').trim()
    })
    return obj
  })
  return { header, records }
}

function sqlString(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function required(value, message) {
  if (!value) throw new Error(message)
  return value
}

function normalize(s) {
  return String(s || '').trim()
}

function main() {
  const root = process.cwd()
  const seedDir = path.join(root, 'supabase', 'seed')
  const citiesCsvPath = path.join(seedDir, 'cities.csv')
  const casinosCsvPath = path.join(seedDir, 'casinos.csv')
  const outPath = path.join(seedDir, 'seed_local_intel.sql')

  const citiesText = fs.readFileSync(citiesCsvPath, 'utf8')
  const casinosText = fs.readFileSync(casinosCsvPath, 'utf8')

  const { records: cityRows } = parseCsv(citiesText)
  const { records: casinoRows } = parseCsv(casinosText)

  const cities = cityRows
    .map((r) => ({
      name: normalize(r.name),
      region: normalize(r.region)
    }))
    .filter((c) => c.name.length > 0)

  const casinos = casinoRows
    .map((r) => ({
      city_name: normalize(r.city_name),
      city_region: normalize(r.city_region),
      casino_name: normalize(r.casino_name)
    }))
    .filter((c) => c.city_name && c.casino_name)

  if (cities.length === 0) {
    throw new Error('No cities found. Fill supabase/seed/cities.csv first.')
  }

  const cityKey = (c) => `${c.name.toLowerCase()}|${(c.region || '').toLowerCase()}`
  const uniqCitiesMap = new Map()
  for (const c of cities) {
    uniqCitiesMap.set(cityKey(c), c)
  }
  const uniqCities = [...uniqCitiesMap.values()]

  // Ensure every casino references a city row
  for (const c of casinos) {
    const key = `${c.city_name.toLowerCase()}|${(c.city_region || '').toLowerCase()}`
    if (!uniqCitiesMap.has(key)) {
      throw new Error(
        `Casino references missing city: ${c.city_name} (${c.city_region || 'no region'}) — add it to cities.csv`
      )
    }
  }

  const lines = []
  lines.push('-- Seed Local Intel data (cities + casinos)')
  lines.push('-- Generated by scripts/generate-local-intel-seed-sql.mjs')
  lines.push('-- Safe to re-run: uses WHERE NOT EXISTS checks (no deletes).')
  lines.push('')
  lines.push('begin;')
  lines.push('')

  lines.push('-- Cities')
  for (const c of uniqCities) {
    const name = required(c.name, 'City name missing')
    const region = c.region || null
    lines.push(
      [
        'insert into public.cities (name, region)',
        `select ${sqlString(name)}, ${region ? sqlString(region) : 'null'}`,
        'where not exists (',
        '  select 1 from public.cities',
        `  where lower(name) = lower(${sqlString(name)})`,
        `    and lower(coalesce(region, '')) = lower(coalesce(${region ? sqlString(region) : 'null'}, ''))`,
        ');'
      ].join('\n')
    )
    lines.push('')
  }

  if (casinos.length > 0) {
    lines.push('-- Casinos')
    for (const c of casinos) {
      const cityName = required(c.city_name, 'casino city_name missing')
      const cityRegion = c.city_region || null
      const casinoName = required(c.casino_name, 'casino casino_name missing')
      lines.push(
        [
          'insert into public.casinos (city_id, name)',
          'select',
          '  (',
          '    select id from public.cities',
          `    where lower(name) = lower(${sqlString(cityName)})`,
          `      and lower(coalesce(region, '')) = lower(coalesce(${cityRegion ? sqlString(cityRegion) : 'null'}, ''))`,
          '    limit 1',
          '  ) as city_id,',
          `  ${sqlString(casinoName)} as name`,
          'where not exists (',
          '  select 1 from public.casinos cs',
          '  join public.cities ct on ct.id = cs.city_id',
          `  where lower(ct.name) = lower(${sqlString(cityName)})`,
          `    and lower(coalesce(ct.region, '')) = lower(coalesce(${cityRegion ? sqlString(cityRegion) : 'null'}, ''))`,
          `    and lower(cs.name) = lower(${sqlString(casinoName)})`,
          ');'
        ].join('\n')
      )
      lines.push('')
    }
  }

  lines.push('commit;')
  lines.push('')

  fs.mkdirSync(seedDir, { recursive: true })
  fs.writeFileSync(outPath, lines.join('\n'), 'utf8')

  console.log(`Wrote ${path.relative(root, outPath)} (${uniqCities.length} cities, ${casinos.length} casinos)`)
}

main()

